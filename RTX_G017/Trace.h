#ifndef _Trace_H
#define _Trace_H

//////////////////////////////////////////////////////////////////////////////////////////

class Trace
{
	int sourcePID;
	int destPID;
	int timeStamp; 
	int msgType;
	int msgID;
	
	public:
	
		Trace (void);
		~Trace (void);
		
		int getSourcePID (void);
		int getDestPID (void);
		int getTimeStamp (void);
		int getMsgType (void);
		int getMsgID (void);
		
		void setSourcePID (int);
		void setDestPID (int);
		void setTimeStamp (int);
		void setMsgType (int);
		void setMsgID (int);
		
		void clear (void);
};

#endif

//////////////////////////////////////////////////////////////////////////////////////////

Trace::Trace(void) { }

//////////////////////////////////////////////////////////////////////////////////////////

Trace::~Trace(void) { }

//////////////////////////////////////////////////////////////////////////////////////////

int Trace::getSourcePID(void){
	return sourcePID;}

//////////////////////////////////////////////////////////////////////////////////////////

int Trace::getDestPID(void){
	return destPID;}

//////////////////////////////////////////////////////////////////////////////////////////
	
int Trace::getTimeStamp(void){
	return timeStamp;}

//////////////////////////////////////////////////////////////////////////////////////////
	
int Trace::getMsgType(void){
	return msgType;}
	
//////////////////////////////////////////////////////////////////////////////////////////
	
int Trace::getMsgID(void){
	return msgID;}

//////////////////////////////////////////////////////////////////////////////////////////
		
void Trace::setSourcePID(int s){
	sourcePID = s;}

//////////////////////////////////////////////////////////////////////////////////////////
	
void Trace::setDestPID(int d){
	destPID = d;}

//////////////////////////////////////////////////////////////////////////////////////////
	
void Trace::setTimeStamp(int t){
	timeStamp = t;}

//////////////////////////////////////////////////////////////////////////////////////////
	
void Trace::setMsgType(int m){
	msgType = m;}
	
//////////////////////////////////////////////////////////////////////////////////////////
	
void Trace::setMsgID(int m){
	msgID = m;}
	
//////////////////////////////////////////////////////////////////////////////////////////

void Trace::clear(void)
{	
	sourcePID = -1;
	destPID = -1;
	timeStamp = -1; 
	msgType = -1;
}

